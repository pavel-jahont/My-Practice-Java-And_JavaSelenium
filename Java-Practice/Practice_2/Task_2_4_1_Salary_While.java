                  // 4. Write a program that increases your salary each month for $ 1,000
                      // USING -     WHILE

public class Salary_While {
   public static void main(String args[]) {
        int salary = 800;
        int month = 1;

while ( month <=12) {
  System.out.println(salary);
  salary+=1000;
  month++;
    }
  }
}
