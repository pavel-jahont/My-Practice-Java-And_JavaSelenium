                  // 4. Write a program that increases your salary each month for $ 1,000
                      // USING -  DO - WHILE

public class Salary_Do_While {
   public static void main(String args[]) {
        int salary = 800;
        int month = 1;

  do {
     System.out.println(salary);
       salary+=1000;
       }
while(month++<12);

  }
}
